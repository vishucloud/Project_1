package com.purplewisteria.bankingservice.bean;

public class BankingContacts {
	
	private String contact;
	private String mail;
	
	public BankingContacts(String contact, String mail) {
		super();
		this.contact = contact;
		this.mail = mail;
	}
	
	public String getContact() {
		return contact;
	}
	public String getMail() {
		return mail;
	}
	
	
	
	

}
